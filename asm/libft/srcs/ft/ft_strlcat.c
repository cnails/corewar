#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t		i;
	size_t		end;

	i = 0;
	while (dst[i] != '\0' && i < size)
		i++;
	end = i;
	while (src[i - end] != '\0' && (i + 1) < size)
	{
		dst[i] = src[i - end];
		i++;
	}
	if (end < size)
		dst[i] = '\0';
	return (end + ft_strlen((char*)src));
}
