#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*ans;
	int		len;

	len = ft_strlen(src);
	ans = (char*)malloc(sizeof(char) * (len + 1));
	if (ans == NULL)
		return (0);
	ans = ft_strcpy(ans, src);
	return (ans);
}
