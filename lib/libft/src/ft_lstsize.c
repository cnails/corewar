#include "../../include/libft.h"

int		ft_lstsize(t_list *begin_list)
{
	int		i;
	t_list	*buff;

	i = 0;
	buff = begin_list;
	while (buff)
	{
		buff = buff->next;
		i++;
	}
	return (i);
}
