#include "../../include/libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*buff;

	i = 0;
	buff = s;
	while (i < n)
	{
		*buff = 0;
		buff++;
		i++;
	}
}
