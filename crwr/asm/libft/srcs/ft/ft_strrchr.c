#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*occur;

	occur = (const char*)0;
	while (*s != '\0')
	{
		if (*s == (char)c)
			occur = s;
		s++;
	}
	if (!c && !*s)
		return ((char *)s);
	return ((char *)occur);
}
