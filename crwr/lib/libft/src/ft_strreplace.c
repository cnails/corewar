#include "../../include/libft.h"

char	*ft_strreplace(char *str, char a, char b)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == a)
			str[i] = b;
		i++;
	}
	return (str);
}
