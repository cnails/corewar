#include "../../include/libft.h"

void	ft_lstswap(t_list **prev, t_list **l1, t_list **l2)
{
	t_list	*tmp;

	tmp = *l1;
	(*prev)->next = *l2;
	(*l1)->next = (*l2)->next;
	(*l2)->next = tmp->next;
}
